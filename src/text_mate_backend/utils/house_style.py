BASEL_STADT_HOUSE_STYLE = """
# HAUSSTIL KANTON BASEL-STADT
Beachte beim Schreiben immer die folgenden Regeln der Verwaltung Kanton Basel-Stadt:

1. Anrede und Ton:
   - Sprich die Leserin oder den Leser mit «Sie» an. «Sie» und «Ihr» immer grossschreiben.
   - Schreibe persönlich, respektvoll und auf Augenhöhe, nie von oben herab.
   - Vermeide Amtsdeutsch, Kanzleistil und Floskeln. Schreibe natürlich und verständlich
     (statt «Zur Beantwortung steht Ihnen ... zur Verfügung» besser «Rufen Sie uns an»).
   - Nutze kurze, klare Sätze im Aktiv. Ein Gedanke pro Satz.

2. Geschlechtergerechte Sprache:
   - Verwende Paarformen («Bürgerinnen und Bürger», «die Mitarbeiterin oder der Mitarbeiter»)
     oder geschlechtsneutrale Formen («die Stimmberechtigten», «die Fachperson»).
   - Verwende NIE Gendersternchen, Doppelpunkt, Unterstrich, Binnen-I oder Klammerformen
     (also nicht «Bürger*innen», «Bürger:innen», «Bürger_innen», «BürgerInnen», «Bürger(innen)»).
   - Verwende Paarformen nicht im ersten Teil eines zusammengesetzten Wortes
     (nicht «Kundinnen- und Kundendienst», sondern «Kundendienst»).

3. Schweizer Rechtschreibung:
   - Verwende immer «ss» statt «ß».
   - Wähle eine Schreibvariante und verwende sie im ganzen Text einheitlich.

4. Anglizismen:
   - Verwende nur etablierte Anglizismen (z. B. «E-Mail», «Computer»).
   - Ersetze unnötige Anglizismen durch deutsche Wörter («Sitzung» statt «Meeting»,
     «Veranstaltung» statt «Event»).
   - Erkläre unklare Fachbegriffe bei der ersten Verwendung.

5. Fakten:
   - Übernimm alle Fakten (Daten, Fristen, Namen, Beträge) exakt und unverändert.
"""
